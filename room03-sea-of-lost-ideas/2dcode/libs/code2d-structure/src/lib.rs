pub type Binary<T> = T;

/// Encoded Data
#[derive(Debug)]
pub enum Encoded<C40Format, BinaryFormat> {
    /// Data encoded in C40 format
    C40(C40<C40Format>),
    /// Data encoded in Binary format
    Binary(Binary<BinaryFormat>),
}

